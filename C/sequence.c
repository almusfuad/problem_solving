#include <stdio.h>


void printSequence(int num) {
    if(num>0) {
        for(int i=num; i>=-num; i--) {
            printf("%d ", i);
        }
    } else {
        for(int i=num; i<=-num; i++) {
            printf("%d ", i);
        }
    }
}


int main()
{
    int n;
    scanf("%d", &n);
    
    printSequence(n);

    return 0;
}
